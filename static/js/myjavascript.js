window.onresize = function () {
    var he1 = document.querySelector('.tupian');
    var height1 = he1.offsetHeight;
    // console.log(height1);
    var nv = document.querySelector('.nav1');
    var ls = document.querySelector('.list1');
    nv.style.height = height1 + 'px';
    var nvh = document.querySelector('.nav1').offsetHeight;
    ls.style.height = nvh - 30 + 'px';
    // console.log(height1);
    // console.log(nv.offsetHeight);
    // console.log(ls.offsetHeight);
    // console.log(ls.children);
    // console.log(ls.children[0].children);
    for (var i = 0; i < ls.children.length; i++) {
        for (var j = 0; j < ls.children[i].children.length; j++) {
            ls.children[i].children[j].style.height = (height1 - 30) / 5 + 'px';
        }
    }
}
var docEl = document.documentElement;
function setRemUnit() {
    var rem = docEl.clientWidth / 120.4; 
    docEl.style.fontSize = rem + 'px'
}

setRemUnit()

window.addEventListener('resize', setRemUnit)
window.addEventListener('pageshow', function (e) {
    if (e.persisted) {
        setRemUnit()
    }
})

